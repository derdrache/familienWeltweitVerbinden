    for (var element in _languages.values) {
      germanLanguages.add(element["nameGer"]);
    }

    return germanLanguages;
  }

  List getAllEnglishLanguages(){
    List englishLanguages = [];

    for (var element in _languages.values) {
      englishLanguages.add(element["nameEng"]!.toLowerCase());
    }

    return englishLanguages;
  }

  List getAllLanguagesIsoCodes(){
    return _languages.keys.toList();
  }

  getLanguageFromIsoCode(isoCode){
    return _languages[isoCode];
  }

  getIsoCode(language) {
    String languageCode = "";

    for (var entry in _languages.entries) {
      if(entry.value["nameGer"] == language || entry.value["nameEng"] == language){
        languageCode = entry.key;
      }
    }
    return languageCode;
  }

  translateLanguageList({germanList, englishList}){
    List translatedList = [];

    if(germanList != null){

      for (var element in _languages.values) {
        if(germanList.contains(element["nameGer"])
            || germanList.contains(element["nameEng"]!.toLowerCase())){
          translatedList.add(element["nameEng"]!.toLowerCase());
        }
      }
    }else{
      for (var element in _languages.values) {
        if(englishList.contains(element["nameEng"]!.toLowerCase())
            || englishList.contains(element["nameGer"])){
          translatedList.add(element["nameGer"]);
        }
      }
    }

    return translatedList;
  }

